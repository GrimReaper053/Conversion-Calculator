#These are Metric => Imperial.
#---------------------------------------------
def grams_To_Ounces(x):
    return x / 28.3495

def kilograms_To_Pounds(x):
    return x / 0.453592

def Metric_Ton_To_US_Ton(x):
    return x / 0.907185

#These are Imperial => Metric.
#---------------------------------------------
def ounces_To_Grams(x):
    return x * 28.3495

def pounds_To_Kilograms(x):
    return x * 0.453592

def US_ton_To_Metric_ton(x):
    return x * 0.907185
