#These are Metric => Imperial.
#---------------------------------------------
def Kilometers_Per_Hour_To_Miles_Per_Hour(x):
    return x / 1.60934

def meters_Per_Second_To_Feet_Per_Second(x):
    return x / 0.3048

#These are Imperial => Metric.
#---------------------------------------------
def miles_Per_Hour_To_Kilometers_Per_Hour(x):
    return x * 1.60934

def feet_Per_Second_To_Meter_Per_Hour(x):
    return x * 0.3048

