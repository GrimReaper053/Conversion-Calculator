#These are Metric => Imperial.
#---------------------------------------------
def cubic_Centimeters_To_Cubic_inches(x):
    return (x**3) / 16.387

def cubic_Meters_To_Cubic_Feet(x):
    return (x**3) / 0.0283168

def cubic_Meters_To_Yards(x):
    return (x**3) / 0.764555

#Imperial => Metric.
#---------------------------------------------
def cubic_Inches_To_Cubic_Centimeters(x):
    return (x**3) * 16.387

def cubic_Feet_To_Cubic_Meters(x):
    return (x**3) * 0.0283168

def cubic_Yards_To_Cubic_Meters(x):
    return (x**3) * 0.764555


