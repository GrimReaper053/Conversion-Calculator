#These are Metric => Imperial.
#---------------------------------------------
def milliliters_To_Fluid_Ounces(x):
    return x / 29.5735

def milliliters_To_Cups(x):
    return x / 236.588

def liters_To_Pints(x):
    return x / 0.473176

def liters_To_Quarts(x):
    return x / 0.946353

def liters_To_Gallons(x):
    return x / 3.78541

#These are Imperial => Metric.
#---------------------------------------------
def fluid_Ounces_To_Milliliters(x):
    return x * 29.5735

def cups_To_Milliliters(x):
    return x * 236.588

def pints_To_Liters(x):
    return x * 0.473176

def quarts_To_Liters(x):
    return x * 0.946353

def gallons_To_Liters(x):
    return x * 3.78541